          <div>
            <div style="font-weight:600;color:var(--facis-text)">No recommendations generated</div>
            <div style="font-size:0.8rem;color:var(--facis-text-secondary)">Ensure simulation is running and price/meter/PV data is available.</div>
          </div>
        </div>

        <div v-else-if="showCards" class="rec-cards-grid">
          <div v-for="rec in filtered" :key="rec.id" class="rec-card card">
            <div class="rec-card__header">
              <div class="rec-icon" :style="{ background: priorityColor(rec.priority) + '18', color: priorityColor(rec.priority) }">
                <i :class="`pi ${categoryIcon(rec.category)}`"></i>
              </div>
              <div class="rec-card__title-block">
                <div class="rec-card__title">{{ rec.title }}</div>
                <div class="rec-card__badges">
                  <span class="uc-badge" :style="{ background: useCaseColor(rec.useCase) + '18', color: useCaseColor(rec.useCase) }">{{ rec.useCase }}</span>
                  <span class="priority-badge" :style="{ background: priorityColor(rec.priority) + '18', color: priorityColor(rec.priority) }">{{ rec.priority }} priority</span>
                </div>
              </div>
            </div>
            <div class="rec-card__summary">{{ rec.summary }}</div>
            <p class="rec-card__explanation">{{ rec.explanation }}</p>
            <div class="rec-card__metrics">
              <div class="rec-metric"><span class="rec-metric__label">Affected Object</span><span class="rec-metric__value">{{ rec.affectedObject }}</span></div>
              <div class="rec-metric"><span class="rec-metric__label">Est. Impact</span><span class="rec-metric__value rec-metric__value--impact">{{ rec.estimatedImpact }}</span></div>
            </div>
            <div class="confidence-row">
              <span class="confidence-label">Confidence</span>
              <div class="confidence-bar-bg"><div class="confidence-bar-fill" :style="{ width: (rec.confidence * 100) + '%', background: rec.confidence >= 0.85 ? 'var(--facis-success)' : 'var(--facis-warning)' }" /></div>
              <span class="confidence-pct">{{ Math.round(rec.confidence * 100) }}%</span>
            </div>
            <div class="advisory-note"><i class="pi pi-lock advisory-note__icon"></i><span>{{ rec.advisoryNote }}</span></div>
          </div>
        </div>

        <div v-else class="card">
          <div class="table-meta"><span class="table-title">{{ filtered.length }} recommendations</span></div>
          <DataTable :value="filtered" row-hover removable-sort scrollable>
            <template #empty><div class="empty-row">No recommendations match your filters.</div></template>
            <Column field="title" header="Title" sortable />
            <Column field="useCase" header="Use Case" sortable style="width:130px">
              <template #body="{ data }"><span class="uc-badge" :style="{ background: useCaseColor(data.useCase) + '18', color: useCaseColor(data.useCase) }">{{ data.useCase }}</span></template>
            </Column>
            <Column field="priority" header="Priority" sortable style="width:100px">
              <template #body="{ data }"><span class="priority-badge" :style="{ background: priorityColor(data.priority) + '18', color: priorityColor(data.priority) }">{{ data.priority }}</span></template>
            </Column>
            <Column field="estimatedImpact" header="Est. Impact" sortable><template #body="{ data }"><span class="impact-text">{{ data.estimatedImpact }}</span></template></Column>
            <Column field="confidence" header="Confidence" sortable style="width:100px">
              <template #body="{ data }"><span class="conf-chip">{{ Math.round(data.confidence * 100) }}%</span></template>
            </Column>
            <Column field="createdAt" header="Created" sortable style="width:160px">
              <template #body="{ data }"><span class="ts">{{ formatDate(data.createdAt) }}</span></template>
            </Column>
          </DataTable>
        </div>
      </template>
    </div>
  </div>
</template>

