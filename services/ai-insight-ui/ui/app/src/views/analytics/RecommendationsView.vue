          <KpiCard label="Energy" :value="summary.energy" trend="stable" icon="pi-bolt" color="#f59e0b" />
          <KpiCard label="Smart City" :value="summary.city" trend="stable" icon="pi-map" color="#8b5cf6" />
        </div>

        <div class="filter-bar">
          <div class="filter-group">
            <span class="filter-group__label">Use Case:</span>
            <button v-for="uc in ['Smart Energy', 'Smart City', 'Cross-Domain']" :key="uc" class="filter-chip" :class="{ 'filter-chip--active': activeUseCaseFilter === uc }" @click="activeUseCaseFilter = activeUseCaseFilter === uc ? null : uc">{{ uc }}</button>
          </div>
          <div class="filter-group">
            <span class="filter-group__label">Category:</span>
            <button v-for="cat in ['cost_saving', 'efficiency', 'maintenance', 'safety']" :key="cat" class="filter-chip" :class="{ 'filter-chip--active': activeCategoryFilter === cat }" @click="activeCategoryFilter = activeCategoryFilter === cat ? null : cat">{{ categoryLabel(cat) }}</button>
          </div>
          <span class="search-wrap" style="margin-left:auto">
            <i class="pi pi-search"></i>
            <InputText v-model="searchQuery" placeholder="Search…" size="small" style="padding-left:2rem;width:220px" />
