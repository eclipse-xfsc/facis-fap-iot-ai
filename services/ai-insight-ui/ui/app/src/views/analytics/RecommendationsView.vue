<script setup lang="ts">
import { ref, computed, onMounted } from 'vue'
import ToggleButton from 'primevue/togglebutton'
import DataTable from 'primevue/datatable'
import Column from 'primevue/column'
import InputText from 'primevue/inputtext'
import PageHeader from '@/components/common/PageHeader.vue'
import KpiCard from '@/components/common/KpiCard.vue'
import { getMeters, getMeterHistory, getPriceHistory, getPVSystems, getPVHistory } from '@/services/api'
import { generateRecommendations } from '@/services/analytics'
import type { Recommendation } from '@/services/analytics'

const loading   = ref(true)
const isLive    = ref(false)
const recommendations = ref<Recommendation[]>([])
const showCards  = ref(true)
const searchQuery = ref('')
const activeUseCaseFilter  = ref<string | null>(null)
const activeCategoryFilter = ref<string | null>(null)

async function fetchData(): Promise<void> {
  loading.value = true
  try {
    const [metersRes, pvRes] = await Promise.all([getMeters(), getPVSystems()])
    if (!metersRes) throw new Error('no data')

    const firstMeterId = metersRes.meters[0]?.meter_id
    const firstPvId    = pvRes?.systems[0]?.system_id

    const [mHist, priceHist, pvHist] = await Promise.all([
      firstMeterId ? getMeterHistory(firstMeterId) : Promise.resolve(null),
      getPriceHistory(),
      firstPvId ? getPVHistory(firstPvId) : Promise.resolve(null)
    ])

    if (priceHist?.prices && mHist?.readings && pvHist?.readings) {
      recommendations.value = generateRecommendations(
        priceHist.prices,
        mHist.readings,
        pvHist.readings
      )
      isLive.value = recommendations.value.length > 0
    }
  } catch {
    recommendations.value = []
  } finally {
    loading.value = false
  }
}

onMounted(() => fetchData())

const filtered = computed(() => {
  let result = [...recommendations.value]
  if (searchQuery.value) {
    const q = searchQuery.value.toLowerCase()
    result = result.filter(r => r.title.toLowerCase().includes(q) || r.explanation.toLowerCase().includes(q))
  }
  if (activeUseCaseFilter.value)  result = result.filter(r => r.useCase === activeUseCaseFilter.value)
  if (activeCategoryFilter.value) result = result.filter(r => r.category === activeCategoryFilter.value)
  return result
})

const summary = computed(() => ({
  total:  recommendations.value.length,
  high:   recommendations.value.filter(r => r.priority === 'high').length,
  medium: recommendations.value.filter(r => r.priority === 'medium').length,
  energy: recommendations.value.filter(r => r.useCase === 'Smart Energy').length,
  city:   recommendations.value.filter(r => r.useCase === 'Smart City').length
}))

function priorityColor(p: string): string {
  if (p === 'high') return '#ef4444'
  if (p === 'medium') return '#f59e0b'
  return '#94a3b8'
}

function categoryIcon(c: string): string {
  switch (c) {
    case 'cost_saving': return 'pi-euro'
    case 'efficiency':  return 'pi-chart-line'
    case 'maintenance': return 'pi-wrench'
    case 'safety':      return 'pi-shield'
    default:            return 'pi-lightbulb'
  }
}

function categoryLabel(c: string): string {
  return c.replace('_', ' ').replace(/\b\w/g, l => l.toUpperCase())
}

function useCaseColor(uc: string): string {
  if (uc === 'Smart Energy') return '#f59e0b'
  if (uc === 'Smart City')   return '#8b5cf6'
  return '#06b6d4'
}

function formatDate(ts: string): string {
  try { return new Date(ts).toLocaleString('en-GB', { day: '2-digit', month: 'short', hour: '2-digit', minute: '2-digit' }) }
  catch { return ts }
}
</script>

<template>
  <div class="view-page">
    <PageHeader
      title="Recommendations"
      subtitle="Cost and efficiency recommendations computed from live price, meter, and PV telemetry — advisory only"
      :breadcrumbs="[{ label: 'Analytics' }, { label: 'Recommendations' }]"
    >
      <template #actions>
        <ToggleButton v-model="showCards" on-label="Cards" off-label="Table" on-icon="pi pi-th-large" off-icon="pi pi-list" size="small" />
      </template>
    </PageHeader>

    <div v-if="isLive" class="live-banner">
      <span class="live-dot"></span> Computed from live price feed + meter + PV telemetry
    </div>

    <div class="view-body">
      <div class="advisory-banner">
        <i class="pi pi-shield advisory-banner__icon"></i>
        <div><strong>Advisory Only — No Automated Control.</strong> All recommendations are generated by the FACIS analytics engine from live telemetry. No action is implemented automatically. All changes require operator review and manual execution.</div>
      </div>

      <div v-if="loading" class="loading-state">
        <i class="pi pi-spin pi-spinner" style="font-size:1.5rem;color:var(--facis-primary)"></i>
        <span>Generating recommendations from live data…</span>
      </div>

      <template v-else>
        <div class="grid-kpi">
          <KpiCard label="Total Recommendations" :value="summary.total" trend="stable" icon="pi-lightbulb" color="#22c55e" />
          <KpiCard label="High Priority" :value="summary.high" trend="stable" icon="pi-flag" color="#ef4444" />
